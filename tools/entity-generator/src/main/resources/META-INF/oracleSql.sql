select 
  UTCM.TABLE_NAME AS TABLE_NAME,
  UTCM.COMMENTS AS TABLE_COMMENTS,
  UCCM.COLUMN_NAME AS COLUMN_NAME,
  UCCM.COMMENTS AS COLUMN_COMMENTS,
  CASE UCCM.COLUMN_NAME 
  	WHEN ? THEN  'true'
  	ELSE 'false'
  END AS ISVERSION	,
  CASE WHEN PK.COLUMN_NAME IS NULL 
    THEN 'false'
    ELSE 'true'
  END AS ISPK,
  CASE DATA_TYPE
    WHEN 'NUMBER' THEN    
      CASE DATA_SCALE 
        WHEN 0 THEN 'java.lang.Long'
        ELSE 'java.math.BigDecimal'
      END
    WHEN 'CHAR' THEN 'java.lang.String'
    WHEN 'NCHAR' THEN 'java.lang.String'
    WHEN 'VARCHAR' THEN 'java.lang.String'
    WHEN 'NVARCHAR' THEN 'java.lang.String'
    WHEN 'VARCHAR2' THEN 'java.lang.String'
    WHEN 'VARCHAR2' THEN 'java.lang.String'
    WHEN 'BLOB' THEN 'java.io.InputStream'
    WHEN 'DATE' THEN 'java.util.Date'
    WHEN 'TIME' THEN 'java.util.Date'
    WHEN 'TIMESTAMP' THEN 'java.util.Date'
    WHEN 'TIMESTAMP(6)' THEN 'java.util.Date'
    ELSE DATA_TYPE
    END AS JAVA_TYPE 
FROM
  USER_TAB_COMMENTS UTCM,
  USER_TAB_COLUMNS UTCL,
  user_col_comments UCCM, 
  (	
  	SELECT 
		  UCC.TABLE_NAME,
		  UCC.COLUMN_NAME 
		FROM 
		  USER_CONS_COLUMNS UCC,
		  (SELECT 
		    TABLE_NAME,
		    CONSTRAINT_NAME 
		  FROM USER_CONSTRAINTS  
		  WHERE CONSTRAINT_TYPE = 'P') P
	WHERE  
		  P.CONSTRAINT_NAME = UCC.CONSTRAINT_NAME
	AND
	  P.TABLE_NAME = UCC.TABLE_NAME
	) PK
WHERE
  UTCM.TABLE_NAME = UTCL.TABLE_NAME
AND
  UCCM.COLUMN_NAME = UTCL.COLUMN_NAME
AND
  UCCM.TABLE_NAME = UTCL.TABLE_NAME  
AND
  UTCL.TABLE_NAME = PK.TABLE_NAME(+)
AND
	UTCL.COLUMN_NAME = PK.COLUMN_NAME(+) 
ORDER BY 
	UTCL.TABLE_NAME ,ISPK desc